	initShow : function(){
        $.ajax({
            url : 'http://localhost:80/projekFix/web_service.php',
            type : 'get',
            beforeSend : function(){
                $.mobile.loading('show', {
                    text : 'Please wait while retrieving data...',
                    textVisible : true
                });
            },
        success : function(dataObject){
		var appendlist = '';
		for (var i = 0; i< dataObject.length; i++) {
			appendlist = appendlist+'<li><a href="#page-two?judul='
			+dataObject[i].judul+'" target="_self" id="detail" data-judul="'
			+dataObject[i].judul+'" class="ui-btn"><img style="margin:8px;" src="' 
            +dataObject[i].img+'"></img><h3>'
			+dataObject[i].judul+'</h3><p>&#128197 '
			+dataObject[i].tgl+'</p><p>&#128336 '
			+dataObject[i].jam+'</p></a></li>';
		}
			$('#list-info').append(appendlist);
        },
        complete : function(){
                $.mobile.loading('hide');
            }
            }); 
	},

	initShowDetail : function(judul){  
         $.ajax({
            url : 'http://localhost:80/projekFix/web_service.php',
            type : 'get',
            beforeSend : function(){
                $.mobile.loading('show', {
                    text : 'Please wait while retrieving data...',
                    textVisible : true
                });
            },
        success : function(dataObject){
		$('#p-p').empty();			
		var appendDetail='';
		for (var i = 0; i< dataObject.length; i++) {
			if(dataObject[i].judul== judul){
				appendDetail = '<img src="'
				+dataObject[i].img+'"></img><h2>'
				+dataObject[i].judul+'</h2><p>&#128197 '
				+dataObject[i].tgl+' | &#128336 '
				+dataObject[i].jam+'</p><p>'
				+dataObject[i].isi+'</p><p><a href="'
				+dataObject[i].url_web+'">More</a></p>';
			};	
		}
			$('#p-p').append(appendDetail);
             },
        complete : function(){
                $.mobile.loading('hide');
            }
            }); 
	},
